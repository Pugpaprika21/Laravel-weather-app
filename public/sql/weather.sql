CREATE TABLE weather_data (
    id INT PRIMARY KEY AUTO_INCREMENT,
    lon FLOAT NOT NULL,
    lat FLOAT NOT NULL,
    weather_id INT NOT NULL,
    weather_main VARCHAR(50) NOT NULL,
    weather_description VARCHAR(100) NOT NULL,
    weather_icon VARCHAR(10) NOT NULL,
    base VARCHAR(50) NOT NULL,
    temp FLOAT NOT NULL,
    feels_like FLOAT NOT NULL,
    temp_min FLOAT NOT NULL,
    temp_max FLOAT NOT NULL,
    pressure INT NOT NULL,
    humidity INT NOT NULL,
    sea_level INT NOT NULL,
    grnd_level INT NOT NULL,
    visibility INT NOT NULL,
    wind_speed FLOAT NOT NULL,
    wind_deg INT NOT NULL,
    wind_gust FLOAT NOT NULL,
    clouds_all INT NOT NULL,
    dt INT NOT NULL,
    sys_type INT NOT NULL,
    sys_id INT NOT NULL,
    sys_country VARCHAR(5) NOT NULL,
    sys_sunrise INT NOT NULL,
    sys_sunset INT NOT NULL,
    timezone INT NOT NULL,
    city_id INT NOT NULL,
    city_name VARCHAR(100) NOT NULL,
    cod INT NOT NULL
);
